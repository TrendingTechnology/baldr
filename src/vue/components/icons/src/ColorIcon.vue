<script lang="ts">
import { Component, Prop } from 'vue-property-decorator'

import { validateColorName } from './main.js'
import PlainIcon from './PlainIcon.vue'

@Component
export default class ColorIcon extends PlainIcon {
  @Prop({
    type: String,
    default: 'black',
    validator: validateColorName
  })
  color: string

  get classes (): string[] {
    let classes = [
      'baldr-icon',
      'vc_color_icon',
      `baldr-icon_${this.name}`,
      `text-${this.color}`
    ]
    return classes
  }
}
</script>
